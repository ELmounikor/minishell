int	ft_isalnum_(int c)
{
	if ((c >= '0' && c <= '9') || (c >= 'A' && c <= 'Z')
		|| (c >= 'a' && c <= 'z') || c == '_')
		return (1);
	return (0);
}

int	ft_isalnumstr(char *s)
{
	int	i;

	i = 0;
	while (s[i] && ft_isalnum_(s[i]))
		i++;
	return (!s[i]);
}
